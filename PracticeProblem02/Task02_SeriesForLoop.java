package PracticeProblem02;

public class Task02_SeriesForLoop {
    public static void main(String[] args) {
        System.out.print("Series 1: ");
        for (int i = 2; i <= 20; i += 2) {
            System.out.print(i + " ");
        }
        System.out.println();

        System.out.print("Series 2: ");
        for (int i = 1; i <= 19; i += 2) {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}

